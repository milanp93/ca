  <ol>
<li>Preheat oven to 350 degrees.</li>
<li>Spread pecans on a baking sheet, and toast in oven for 5-7 minutes until fragrant. Set aside.</li>
<li>Line a 9x9 pan with parchment paper, extending the paper over the sides. Spray lightly with non-stick cooking spray. Set aside.</li>
<li>Melt butter in a medium pot over medium-low heat. Cook for 3-4 minutes until golden brown, stirring occasionally. Watch carefully so it doesn't burn.</li>
<li>Remove from heat and let cool for a few minutes.
<li>Add brown sugar and eggs, one at a time stirring to incorporate.</li>
<li>Add vanilla and mix well.</li>
<li>In a separate bowl whisk together flour, salt and baking powder.</li>
<li>Add to butter mixture and mix until well combined.</li>
<li>Add ½ cup cranberries, ½ cups white chocolate chunks and ½ cups toasted pecans.</li>
<li>Pour batter into prepared pan. Sprinkle with extra ¼ cup white chocolate chunks and ¼ cup toasted pecans.</li>
<li>Bake 30-35 minutes until toothpick comes out mostly clean.</li>
<li>Cool before cutting into bars.</li>
  </ol>
  
  
  
